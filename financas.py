#classe finanças com patrimonio, salario, gastos e investimentos
class Financas:
    def __init__(self, patrimonio, salario, gastos, investimentos):
        self.patrimonio = patrimonio
        self.salario = salario
        self.gastos = gastos
        self.investimentos = investimentos
